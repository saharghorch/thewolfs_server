package Utils;


public class Bourse {

	private String  price ;
	private String name ;
	private String haut;
	private String bas;
	private String volumeTitre;
	private String volumeDt;
	private String cloture;
    private String variation;	


	public String getPrice() {
		return price;
	}
	public void setPrice(String price) {
		this.price = price;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getHaut() {
		return haut;
	}
	public void setHaut(String haut) {
		this.haut = haut;
	}
	public String getBas() {
		return bas;
	}
	public void setBas(String bas) {
		this.bas = bas;
	}
	public String getVolumeTitre() {
		return volumeTitre;
	}
	public void setVolumeTitre(String volumeTitre) {
		this.volumeTitre = volumeTitre;
	}
	public String getVolumeDt() {
		return volumeDt;
	}
	public void setVolumeDt(String volumeDt) {
		this.volumeDt = volumeDt;
	}
	public String getCloture() {
		return cloture;
	}
	public void setCloture(String cloture) {
		this.cloture = cloture;
	}
	public String getVariation() {
		return variation;
	}
	public void setVariation(String variation) {
		this.variation = variation;
	}
	
	
	
	
}

